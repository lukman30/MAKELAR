package com.baba.makelar.model;

public class ModelInfoProduk {
    int ikon;
    String key, komponen;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public int getIkon() {
        return ikon;
    }

    public void setIkon(int ikon) {
        this.ikon = ikon;
    }

    public String getKomponen() {
        return komponen;
    }

    public void setKomponen(String komponen) {
        this.komponen = komponen;
    }
}
